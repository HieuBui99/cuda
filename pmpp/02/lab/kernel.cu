// #include <__clang_cuda_builtin_vars.h>
#include <cstdio>
__global__ void vecAddKernel(float *a, float *b, float *c, int n) {
    int i = blockIdx.x * blockDim.x + threadIdx.x;
    if (i < n) {
        c[i] = a[i] + b[i];
    }
}

